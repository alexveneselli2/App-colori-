/**
 * Iride — i18n
 * Two supported languages: English (en) and Italian (it).
 * System default: auto-detected from navigator.language.
 */

export type Lang = 'en' | 'it'
export type LangPref = 'en' | 'it' | 'system'

export function detectSystemLang(): Lang {
  try {
    const l = navigator.language.toLowerCase()
    if (l.startsWith('it')) return 'it'
  } catch { /* ignore */ }
  return 'en'
}

